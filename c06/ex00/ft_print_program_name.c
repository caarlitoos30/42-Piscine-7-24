#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int arg, char **argv)
{
	if (arg < 0)
		return (0);
	ft_putstr(argv[0]);
	write(1, "\n", 1);
}
